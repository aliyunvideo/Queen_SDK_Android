package io.agora.vlive.protocol.model.model;

public class MusicInfo {
    String musicId;
    String musicName;
    String singer;
    String url;

    public String getMusicid() {
        return musicId;
    }

    public String getMusicName() {
        return musicName;
    }

    public String getSinger() {
        return singer;
    }

    public String getUrl() {
        return url;
    }
}
